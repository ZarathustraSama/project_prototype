function love.update(dt)
      
    -- Time out how far apart our shots can be.
    canShootTimer = canShootTimer - (dt)
    if canShootTimer < 0 then
      canShoot = true
    end
    -- Update all the objects
    for i,v in ipairs(objects) do
        v:update(dt)        
    end   
    
    -- Time out enemy creation
    createEnemyTimer = createEnemyTimer - (1 * dt)
    if createEnemyTimer < 0 then
      createEnemyTimer = createEnemyTimerMax

      -- Create an enemy
      randomNumber = love.math.random(love.graphics.getWidth())
      table.insert(listOfEnemies, Enemy(randomNumber, 10))
    end
    
    -- update the positions of enemies
    for i, enemy in ipairs(listOfEnemies) do
      enemy.y = enemy.y + (200 * dt)

      if enemy.y > 515 then -- remove enemies when they pass off the screen
        table.remove(listOfEnemies, i)
      end
    end
    
    for i,v in ipairs(listOfBullets) do
        v:update(dt)        
        --Each bullets checks if there is collision with the enemy
        for i,bullet in ipairs(listOfBullets) do
          for j,enemy in ipairs(listOfEnemies) do
            local collision = enemy:resolveCollision(bullet)
            if collision then
              table.remove(listOfBullets, i)              
            end
          end
          if bullet.x > width or bullet.x < 0 then
            table.remove(listOfBullets, i)
          end
        end
    end
    
    for i,v in ipairs(ground) do
      v:update(dt)
    end
    
    for i,v in ipairs(ground_a) do
      v:update(dt)
    end
    
    for i,v in ipairs(background) do
      v:update(dt)
    end

    local loop = true
    local limit = 0

    while loop do
        -- Set loop to false, if no collision happened it will stay false
        loop = false

        limit = limit + 1
        if limit > 100 then
            -- Still not done at loop 100
            -- Break it because we're probably stuck in an endless loop.
            break
        end

        for i=1,#objects-1 do
            for j=i+1,#objects do
                local collision = objects[i]:resolveCollision(objects[j])
                if collision then
                    loop = true
                end
            end
        end
        
        --For each object check collision with every wall
        for i,ground in ipairs(ground) do
          for j,object in ipairs(objects) do
            local collision = object:resolveCollision(ground)
            if collision then
              loop = true
            end
          end
        end
    end
end

function love.draw() 
  --Draw the background
  for i,v in ipairs(background) do
    v:draw()
  end
  
  --Draw the walls
  for i,v in ipairs(ground) do
    v:draw()
  end
  
  for i,v in ipairs(ground_a) do
    v:draw()
  end
  
  -- Draw all the objects
  for i,v in ipairs(objects) do
    v:draw()
  end
  
  for i,v in ipairs(listOfBullets) do
        v:draw()
  end
end

function love.keypressed(key)
    -- Let the player jump
    if key == "space" then
        player:jump()
    end
    
    -- Let the player shoot
    if key == "z" and canShoot then
        player:shoot()
        canShoot = false
        canShootTimer = canShootTimerMax
    end      
    
    --Exits the game
    if key == "escape" then
        love.event.quit()
    end
end

function love.mouse.isDown(button)
    --Let the player shoot with the mouse
    if button == 1 and canShoot then
      player:shoot()
      canShoot = false
      canShootTimer = canShootTimerMax
    end
end